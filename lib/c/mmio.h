// GPIO-related MMIO registers
#define PINB 0x23
#define DDRB 0x24
#define PORTB 0x25

#define PINc 0x26
#define DDRC 0x27
#define PORTC 0x28

#define PIND 0x29
#define DDRD 0x2A
#define PORTD 0x2B

// Analog-Digital converter registers
#define ADCL 0x78
#define ADCH 0x79

#define ADCSRA 0x7A
#define ADCSRB 0x7B
#define ADMUX 0x7C
